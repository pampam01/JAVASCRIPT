// typeof  merupakan operator yang bisa kita gunakan untuk melihat tipe dari suatu data
let data;
let typeData = typeof data;
console.log(typeData);

data = "pampam";
typeData = typeof data;
console.log(typeData);

data = [];
typeData = typeof data;
console.log(typeData);

data = null;
typeData = typeof data;
console.log(typeData);

data = {};
typeData = typeof data;
console.log(typeData);

data = 12.2;
typeData = typeof data;
console.log(typeData);

data = 100;
typeData = typeof data;
console.log(typeData);

data = false;
typeData = typeof data;
console.log(typeData);